class Solution{
public:
    int evenlyDivides(int N){
        //code here
        int num = N;
        int ans = 0;
        while(num){
            int digit = num%10;
            num = num/10;
            if(digit!=0 && N % digit == 0)
                ans++;
        }
        return ans;
    }
};

